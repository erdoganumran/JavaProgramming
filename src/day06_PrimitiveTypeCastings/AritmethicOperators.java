package day06_PrimitiveTypeCastings;

public class AritmethicOperators {
    public static void main(String[] args) {

        System.out.println("12"+1);
        System.out.println(100-50);
        System.out.println(10*6);
        System.out.println(80+20);

        System.out.println(100/3);//33
        System.out.println(100.0/3);//33.33333
        System.out.println((double)100/3);//33.33333
        System.out.println(100/3.0);//3.33333
        System.out.println(100d/3);//33.3333

        int a = 100;
        double b = a/6;//16.0

        System.out.println(b);

        double c=a/6.0; //16.66
        System.out.println(c);

        System.out.println(100D);


    }


}
