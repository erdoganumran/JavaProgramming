package day17_While_DoWhile;

public class WhileLoopIntro {
    public static void main(String[] args) {
         //always give boolean ex. if boolean is true, statement will be executed.
        if(true){
            System.out.println("Wooden Spoon");// it will be executed just one time
            //while loop if is repeated if statement
        }
        System.out.println("----------------------------");

        while (true){
            System.out.println("Wooden Spoon");
        }






    }
}
